CREATE TABLE INSTRUCTOR(
instructorID NUMBER PRIMARY KEY,
instructorName VARCHAR2(45)
);
CREATE TABLE MODULE(
moduleCode VARCHAR2(10) PRIMARY KEY,
moduleName VARCHAR2(45)
);
CREATE TABLE STUDENT(
studentNo NUMBER PRIMARY KEY,
studentName VARCHAR2(45)
);
CREATE TABLE REGISTRATION(
moduleCode VARCHAR2(10),
studentNo NUMBER,
FOREIGN KEY(moduleCode)
REFERENCES MODULE(moduleCode),
FOREIGN KEY(studentNo)
REFERENCES STUDENT(studentNo)
); 

CREATE SEQUENCE instructorID
START WITH 1
INCREMENT BY 1
NOCACHE;
CREATE SEQUENCE studentNo
START WITH 1
INCREMENT BY 1
NOCACHE;

