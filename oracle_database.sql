into i_count
FROM REGISTRATION
WHERE ModuleCode = n_ModuleCode
AND studentNo = n_student;
IF i_count = 0 THEN
INSERT INTO REGISTRATION (ModuleCode, studentNo)
VALUES (n_ModuleCode, n_student);
